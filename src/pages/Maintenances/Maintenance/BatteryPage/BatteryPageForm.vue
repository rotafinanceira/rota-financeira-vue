<script setup lang="ts">
import { ref, onMounted } from 'vue';
import { useRouter, useRoute } from 'vue-router';
import { vMaska } from 'maska/vue';
import type { MaskInputOptions } from 'maska';
import { AxiosError } from 'axios';
import { storeToRefs } from 'pinia';

import CButton from '@/shared/components/CButton.vue';
import CModal from '@/shared/components/CModal.vue';
import CInput from '@/shared/components/CInput.vue';
import CSelect from '@/shared/components/CSelect.vue';
import CFormatedInput from '@/shared/components/CFormatedInput.vue';
import helpIcon from '@/shared/assets/helpIcon.svg';

import { useBatteryStore } from '@/stores/batteryStore';
import { useCarStore } from '@/stores/carStore';
import {
  formatInput,
  parseInputToNumber,
} from '@/shared/helper/inputFormatHelper';

const batteryStore = useBatteryStore();
const carStore = useCarStore();
const router = useRouter();
const route = useRoute();

const { maintenances, isLoading } = storeToRefs(batteryStore);

const maintenanceId = route.params.maintenanceId as string | undefined;

const date = ref('');
const mileage = ref('');
const capacity = ref('');
const brand = ref('');
const maintenanceValue = ref('R$ 0,00');
const oficina = ref('');

const dateMask: MaskInputOptions = {
  mask: '##/##/####',
  tokens: { '#': { pattern: /\d/ } },
};

interface Option {
  label: string;
  value: string;
}

const capacityOptions: Option[] = [
  { label: '60 Ah', value: '60' },
  { label: '80 Ah', value: '80' },
  { label: '120 Ah', value: '120' },
];

const isOpen = ref(false);
const modalContent = ref('Quando devo fazer a troca?');
const modalDescription = ref<string[]>([]);
const isPositiveOpen = ref(false);
const isErrorOpen = ref(false);

const successTitle = ref('Parabéns!');
const successDescription = ref(
  'Você cadastrou o monitoramento de bateria seu veículo. Iremos lhe informar sobre a próxima troca.'
);

const errorTitle = ref('Algo deu errado!');
const errorDescription = ref(
  'Suas alterações não foram salvas. Tente novamente mais tarde.'
);

function showHelpModal() {
  isOpen.value = true;
  modalContent.value = 'Quando devo fazer a troca?';
  modalDescription.value = [
    'Substitua a bateria se já tiver mais de 3 anos de uso;',
    'Troque a bateria se o carro apresentar dificuldade para ligar;',
    'Verifique a carga da bateria se as luzes internas estiverem fracas;',
    'Limpe os terminais ou troque a bateria em caso de corrosão visível. ',
  ];
}

function closeSuccess() {
  isPositiveOpen.value = false;
  router.push({ name: 'maintenance-battery' });
}

interface BatteryMaintenancePayload {
  lastMaintenanceDate: string;
  lastMaintenanceKm: number;
  remainingCapacity: number;
  valor: number;
  batteryBrand: string;
  oficina: string;
}

async function handleSubmit() {
  if (!carStore.firstLicensePlate) {
    errorTitle.value = 'Erro ao salvar manutenção';
    errorDescription.value = 'Nenhum carro selecionado.';
    isErrorOpen.value = true;
    return;
  }

  isLoading.value = true;

  try {
    const [day, month, year] = date.value.split('/');
    const isoDate = `${year}-${month}-${day}`;

    const payload: BatteryMaintenancePayload = {
      lastMaintenanceDate: isoDate,
      lastMaintenanceKm: parseInputToNumber(mileage.value),
      remainingCapacity: Number(capacity.value),
      valor: parseInputToNumber(maintenanceValue.value),
      batteryBrand: brand.value,
      oficina: oficina.value,
    };

    if (maintenanceId) {
      successTitle.value = 'Edição concluída!';
      successDescription.value = 'As alterações foram salvas com sucesso.';
    }

    await batteryStore.saveBatteryMaintenance(
      payload,
      batteryStore.getEditingId
    );

    batteryStore.setSelectedMaintenance(null);
    isPositiveOpen.value = true;
  } catch (err) {
    const error = err as AxiosError<{ message?: string }>;
    console.error('Erro ao salvar manutenção:', error);
    isErrorOpen.value = true;
  } finally {
    isLoading.value = false;
  }
}

onMounted(async () => {
  if (!carStore.firstLicensePlate) {
    await carStore.getCars();
  }

  const plate = carStore.firstLicensePlate;
  if (!plate) return;

  if (!maintenances.value || maintenances.value.length === 0) {
    const resp = await batteryStore.getBatteryMaintenances(plate);
    console.log(resp);
  }

  if (maintenanceId) {
    const m = maintenances.value.find((m) => m.id === maintenanceId);
    if (!m) return;

    batteryStore.setSelectedMaintenance(m);

    date.value = m.lastMaintenanceDate
      ? new Date(m.lastMaintenanceDate).toLocaleDateString('pt-BR')
      : '';

    mileage.value = formatInput(m.lastMaintenanceKm ?? 0);
    maintenanceValue.value = formatInput(m.valor ?? 0);
    brand.value = m.batteryBrand ?? '';
    capacity.value = m.remainingCapacity.toString() ?? '';
    oficina.value = m.oficina ?? '';
  }
});
</script>

<template>
  <div class="main-content">
    <div class="card-wrapper">
      <div class="card">
        <div class="text-wrapper">
          <div class="header-content">
            <span class="title">Manutenção</span>
            <div @click="showHelpModal">
              <img :src="helpIcon" alt="Help Icon" />
            </div>
          </div>
          <span class="subtitle">
            Preencha as informações da manutenção de Troca de Óleo.
          </span>
        </div>

        <div class="input-wrapper">
          <CFormatedInput
            v-model="mileage"
            label="Km na data de serviço*"
            name="mileage"
            variant="unit"
          />
        </div>

        <div class="input-wrapper">
          <CInput
            :value="date"
            v-model="date"
            label="Data da troca*"
            name="last-battery-change"
            placeholder="DD/MM/AAAA"
            variant="generic"
            v-maska="dateMask"
          />
        </div>

        <div class="input-wrapper">
          <CSelect
            v-model="capacity"
            name="capacity"
            label="Amperagem*"
            :options="capacityOptions"
            placeholder="Selecione uma opção"
          />
        </div>

        <div class="input-wrapper">
          <CFormatedInput
            v-model="maintenanceValue"
            label="Valor da manutenção*"
            name="maintenanceValue"
            variant="money"
          />
        </div>

        <div class="input-wrapper">
          <CInput
            :value="brand"
            v-model="brand"
            label="Marca"
            name="battery-brand"
            placeholder="Digite a marca utilizada"
            variant="generic"
          />
        </div>

        <div class="input-wrapper">
          <CInput
            :value="oficina"
            v-model="oficina"
            label="Oficina"
            name="oficina"
            placeholder="Digite o nome da oficina"
            variant="generic"
          />
        </div>
      </div>
    </div>

    <CButton @click="handleSubmit" :isLoading="isLoading">Salvar</CButton>

    <!-- Modal de ajuda -->
    <CModal v-model="isOpen" variant="info">
      <h2>{{ modalContent }}</h2>
      <ul class="info-list">
        <li v-for="(item, index) in modalDescription" :key="index">
          {{ item }}
        </li>
      </ul>
    </CModal>

    <CModal
      v-model="isPositiveOpen"
      icon="success"
      variant="default"
      @update:modelValue="
        (val) => {
          if (!val) closeSuccess();
        }
      "
    >
      <div class="group">
        <h2>{{ successTitle }}</h2>
        <p>{{ successDescription }}</p>
      </div>
    </CModal>

    <CModal
      v-model="isErrorOpen"
      icon="error"
      variant="default"
      @update:modelValue="
        (val) => {
          if (!val) isErrorOpen = false;
        }
      "
    >
      <div class="group">
        <h2>{{ errorTitle }}</h2>
        <p>{{ errorDescription }}</p>
      </div>
    </CModal>
  </div>
</template>

